#import "ZHCar.h"
#import "ZHConstants.h"

@interface ZHCar()
@property(nonatomic, assign) float money;

@end

@implementation ZHCar

#pragma mark -
#pragma mark initialize / deallocate

- (void)dealloc {
    [super dealloc];
}

- (instancetype)init {
    self = [super init];
    self.money = kZHCarMoney;
    
    return self;
}

#pragma mark -
#pragma mark Public methods

- (void)receiveMoney:(float)number {
    self.money += number;
}

- (void)giveMoney:(float)number {
    self.money -= number;
}

@end
